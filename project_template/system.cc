/**
  * @brief  This function is executed in case of error occurrence.
  * @param  None
  * @retval None
  */
void Error_Handler (void)
{
  /* User may add here some code to deal with this error */
  while(1)
  {
	  BSP_LED_Toggle(LED3);
	  HAL_Delay(100);
  }
}

void Error_Check (void) {
	  BSP_LED_Toggle(LED3);
	  HAL_Delay(1000);
	  BSP_LED_Toggle(LED3);
	  HAL_Delay(1000);
}
